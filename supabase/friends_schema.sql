-- ============================================================================
-- VERIFICATION QUERIES — run to confirm tables exist and RLS is on
-- ============================================================================
SELECT
    table_name,
    pg_size_pretty(pg_total_relation_size(quote_ident(table_name)::regclass)) AS size
FROM information_schema.tables
WHERE table_schema = 'public'
  AND table_name IN ('friendships', 'invite_tokens', 'portfolio_snapshots')
ORDER BY table_name;

SELECT
    tablename,
    rowsecurity
FROM pg_tables
WHERE schemaname = 'public'
  AND tablename IN ('friendships', 'invite_tokens', 'portfolio_snapshots');


